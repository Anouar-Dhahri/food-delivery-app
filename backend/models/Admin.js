import mongoose from 'mongoose'

const AdminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  surname: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  }
},{ timestamps: true })

const Admin = mongoose.model('Admin', AdminSchema);

export { Admin }